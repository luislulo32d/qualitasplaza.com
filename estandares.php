                            <p>Reglas de usabilidad de Jakob Nielsen:</p>
                            <ul>
                              <li>Visibilidad del estado del sistema: Es de vital importancia que tu usuario esté permanentemente informado sobre lo que está pasando cuando interactúa en tu Web, ya que intrernamente ocurren acciones que no puede ver, y que le pueden producir incertidumbre en muchas situaciones.</li>
                              <li>Consistencia entre el sistema y el mundo real: El Sitio Web tiene que comunicarse con el usuario en su mismo lenguaje. Y esto no se refiere sólo a la calidad del texto, sino también a elementos como:
                                <ul>
                                  <li>Imágenes.</li>
                                  <li>Orden en los procesos.</li>
                                  <li>Métodos para presentar la información.</li>
                                </ul>
                              </li>
                              <li>El usuario es libre y tiene el control: No debes forzar al usuario a seguir un camino determinado. Evita llevarlo a lugares sin salida. El usuario debe tener siempre la posibilidad de navegar libremente, encontrar con facilidad los accesos a la salida y botones a rutas alternativas, además de habilitar todas las facilidades que necesite para “hacer” y “deshacer”.</li>
                              <li>Consistencia y estándares: Tu Sitio Web debe seguir un estándar consistente en todas sus páginas. Ejemplo: No es recomendable mostrar un menú funcione de manera diferente en distintas secciones de la Web, ni cambiarlo de ubicación. No puedes usar distintos diseños para un mismo elemento en distintos apartados (forma, color,fuente), ni una terminología variable.</li>
                              <li>Prevención de Errores: Antes de crear mensajes de error bien elaborados, se debe enfocar todo el trabajo en crear un producto de alta calidad para evitar los errores.</li>
                              <li>Reconocimiento antes que recuerdo: Se deben tener visibles las acciones y opciones. El usuario no debería recordar la información que se le da en una parte del proceso para seguir adelante. Las instrucciones de uso del portal deben estar a la vista o ser fácilmente accesibles en el momento en que sea necesario.</li>
                              <li>Flexibilidad y eficiencia de uso: la presencia de aceleradores, que no son vistos por los usuarios novatos, puede ofrecer una interacción más rápida a los usuarios expertos que la que el sistema puede proveer a los usuarios de todo tipo. Se debe permitir que los usuarios adapte el sistema para usos frecuentes.</li>
                              <li>Estética y diseño minimalista: los diálogos no deben contener información que es irrelevante o poco usada. Cada unidad extra de información en un diálogo, compite con las unidades de información relevante y disminuye su visibilidad relativa.</li>
                              <li>Ayudar a los usuarios a reconocer, diagnosticar y recuperarse de errores: los mensajes de error se deben entregar en un lenguaje claro y simple, indicando en forma precisa el problema y sugerir una solución constructiva al problema.</li>
                              <li>Ayuda y documentación: incluso en los casos en que el sistema pueda ser usado sin documentación, podría ser necesario ofrecer ayuda y documentación. Dicha información debería ser fácil de buscar, estar enfocada en las tareas del usuario, con una lista concreta de pasos a desarrollar y no ser demasiado extensa.</li>
